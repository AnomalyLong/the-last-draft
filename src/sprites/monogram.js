const CHAR_W = 5;
const CELL_W = 6;   // 5px glyph + 1px gap
const GLYPH_H = 7;  // rows 3-9
const ROW_OFFSET = 3;

export const MONOGRAM_CELL_W = CELL_W;
export const MONOGRAM_GLYPH_H = GLYPH_H;

// Returns [[px, py], ...] pixel positions for all lit pixels in `text`.
// x, y = top-left of the first character in SVG coordinates.
export function pixelTextPixels(text, x, y, scale = 2) {
  const pixels = [];
  for (let ci = 0; ci < text.length; ci++) {
    const rows = GLYPHS[text[ci].toUpperCase()] ?? GLYPHS[' '];
    const charX = x + ci * CELL_W * scale;
    for (let row = 0; row < GLYPH_H; row++) {
      const bits = rows[ROW_OFFSET + row] || 0;
      for (let col = 0; col < CHAR_W; col++) {
        if ((bits >> col) & 1) {
          pixels.push([charX + col * scale, y + row * scale]);
        }
      }
    }
  }
  return pixels;
}
